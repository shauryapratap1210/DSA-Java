import java.util.*;
public class sum_of_twoinputnumbers {
    public static void main(String[] args) {
        Scanner sc= new Scanner(System.in);
        System.out.println("Enter New numbers");
        int a= sc.nextInt();
        int b= sc.nextInt();
        int sum=a+b;
        System.out.println("Sum of two numbers is : "+sum);
    }
}
